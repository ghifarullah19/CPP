#include <iostream>
using namespace std;

int main() {

    // Boolean Types
    /*
     A boolean data type is declared with the bool keyword
     and can only take the values true or false
     true = 1
     false = 0
    */
    bool isCodingFun = true;
    bool isFishTasty = false;
    cout << isCodingFun << endl;
    cout << isFishTasty << endl;
    return 0;
}
