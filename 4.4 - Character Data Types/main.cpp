#include <iostream>
using namespace std;

int main() {

    // Character Types
    /*
     The char type is used to store a single character.
     The character must be surrounded by single quotes,
     like 'A' or 'c':
    */
    char myGrade = 'B';
    cout << myGrade << endl;

    /*
     alternatively, you can use ASCII values to display
     certain characters:
    */
    char a = 65, b = 66, c = 67;
    cout << a << endl;
    cout << b << endl;
    cout << c << endl;
    return 0;
}
