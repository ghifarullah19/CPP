#include <iostream>
using namespace std;

int main() {

    // Assignment Operators
    /*
     Assignment operators are used to assign values to variables.
    */
    int x = 10;

    /*
     The addition assignment operator (+=) adds a value to a variable:
    */
    int y = 10;
    y += 5;

    /*
     Operator	Example	    Same As
        =	    x = 5	    x = 5
        +=	    x += 3	    x = x + 3
        -=	    x -= 3	    x = x - 3
        *=	    x *= 3	    x = x * 3
        /=	    x /= 3	    x = x / 3
        %=	    x %= 3	    x = x % 3
        &=	    x &= 3	    x = x & 3
        |=	    x |= 3	    x = x | 3
        ^=	    x ^= 3	    x = x ^ 3
        >>=	    x >>= 3	    x = x >> 3
        <<=	    x <<= 3	    x = x << 3
    */
    return 0;
}
